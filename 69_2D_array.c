#include <stdio.h>

int main() {
  int arr[3][3] = {{11, 12, 13}, {14, 15, 16}, {17, 18, 19}};
  int *p[4] = {*arr, *(arr + 1), *(arr + 2)};

  printf("%d %d \n", arr[0][0], **p);
  // **p
  // **500
  // *100
  // 11

  printf("%d %d \n", arr[0][1], *(*p + 1));
  // *(*p+1)
  // *(*500+1)
  // *(100+1)
  // *104
  // 12

  printf("%d %d \n", arr[0][2], *(*p + 2));

  printf("%d %d \n", arr[1][0], *(*(p + 1)));
  // *(*(p+1)))
  // *(*(500+1))
  // *(*(508))
  // *(112)  
  // 14

  printf("%d \n", *(*(p + 2)));
  printf("%d \n", *(*(p + 1) + 2));
  printf("%d \n", *(*(p + 2) + 1));
  printf("%d \n", (*(*(p + 1) + 1)) + 20);

  return 0;
}